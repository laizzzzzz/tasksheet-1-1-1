public class tasksheet_1_1_2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        a += b; 
        System.out.println("After += : " + a);

        a -= b; 
        System.out.println("After -= : " + a);

        a *= b; 
        System.out.println("After *= : " + a);

        a /= b;
        System.out.println("After /= : " + a);

        a %= b; 
        System.out.println("After %= : " + a);
    }
}
